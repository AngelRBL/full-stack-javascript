//--->VAR
var numero1 = 10
console.log(numero1)
//---> es una manera para declarar una variable
//---Utilizar var lo menos posible por medidas de seguridad

//--->LET
// da el mismo resultado que var la unica diferencia es el scope
{
    let num1 = 10
}
console.log(num1)
// el alcance de let se queda dentro de las llaves
